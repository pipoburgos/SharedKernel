namespace SharedKernel.Api;

/// <summary> . </summary>
public static class CacheDuration
{
    /// <summary> . </summary>
    public const int Minute = 60;

    /// <summary> . </summary>
    public const int TenMinutes = Minute * 10;

    /// <summary> . </summary>
    public const int Hour = Minute * 60;

    /// <summary> . </summary>
    public const int Day = Hour * 24;

    /// <summary> . </summary>
    public const int Week = Day * 7;

    /// <summary> . </summary>
    public const int Month = Day * 30;

    /// <summary> . </summary>
    public const int Year = Day * 365;
}

